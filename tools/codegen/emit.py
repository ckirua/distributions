def build_cydist_specs(registry: list[dict], recipes: dict[str, Recipe]) -> list[CydistSpec]:
    manual_params: dict[str, list[tuple[str, str, str]]] = {
        "bernoulli": [("p", "double", "scalar_double")],
        "discrete-uniform": [
            ("low", "int", "scalar_int"),
            ("high", "int", "scalar_int"),
        ],
        "binomial": [("trials", "int", "scalar_int"), ("p", "double", "scalar_double")],
        "geometric": [("p", "double", "scalar_double")],
        "exponential": [("rate", "double", "scalar_double")],
        "normal-gaussian": [("mu", "double", "scalar_double"), ("sigma", "double", "scalar_double")],
        "categorical": [("probs", "const double*", "probs")],
        "skellam": [("mu1", "double", "scalar_double"), ("mu2", "double", "scalar_double")],
        "poisson-binomial": [("probs", "const double*", "probs")],
        "beta-binomial": [
            ("trials", "int", "scalar_int"),
            ("alpha", "double", "scalar_double"),
            ("beta", "double", "scalar_double"),
        ],
        "negative-binomial": [("r", "int", "scalar_int"), ("p", "double", "scalar_double")],
        "zipf": [("N", "int", "scalar_int"), ("s", "double", "scalar_double")],
        "zipfmandelbrot": [
            ("N", "int", "scalar_int"),
            ("q", "double", "scalar_double"),
            ("s", "double", "scalar_double"),
        ],
    }

    specs: list[CydistSpec] = []
    for entry in registry:
        vid = entry["id"]
        discrete = entry.get("type") == "discrete"
        meta = dict(
            name=entry.get("name", vid),
            category_path=entry.get("category_path", "unknown"),
            summary=entry.get("summary") or "",
            sampler_tier="hand-written" if vid in MANUAL else recipes[vid].sampler_tier,
        )

        if vid in MANUAL_CPP_CLASS:
            specs.append(
                CydistSpec(
                    vault_id=vid,
                    py_func=python_func_name(vid),
                    c_func=c_func_name(vid),
                    cpp_class=MANUAL_CPP_CLASS[vid],
                    discrete=discrete,
                    params=manual_params[vid],
                    **meta,
                )
            )
            continue

        r = recipes[vid]
        params: list[tuple[str, str, str]] = []
        for ctype, name, _default in r.members:
            if ctype == "int":
                params.append((name, "int", "scalar_int"))
            else:
                params.append((name, "double", "scalar_double"))
        specs.append(
            CydistSpec(
                vault_id=vid,
                py_func=python_func_name(vid),
                c_func=c_func_name(vid),
                cpp_class=r.cpp_class,
                discrete=r.discrete,
                params=params,
                **meta,
            )
        )
    return specs


def _cydist_ctor_body(
    spec: CydistSpec, ctor_args: list[str], shim_body_prefix: list[str], cpp_class: str
) -> list[str]:
    if shim_body_prefix:
        return shim_body_prefix
    if ctor_args:
        return [f"    distributions::{cpp_class} dist({', '.join(ctor_args)});"]
    return [f"    distributions::{cpp_class} dist;"]


def _append_cydist_shim(
    *,
    c_func: str,
    c_params: list[str],
    body: list[str],
    shim_decls: list[str],
    shim_impl: list[str],
) -> None:
    shim_decls.append(f"void {c_func}({', '.join(c_params)});")
    shim_impl.append(f"void {c_func}({', '.join(c_params)}) {{")
    shim_impl.extend(body)
    shim_impl.append("    distributions::Pcg32 rng(seed);")
    shim_impl.append("    dist.sample_batch(out, n_samples, rng);")
    shim_impl.append("}")
    shim_impl.append("")


def emit_cydist(specs: list[CydistSpec]) -> None:
    shim_decls: list[str] = []
    shim_impl: list[str] = []
    pyx_extern: list[str] = []
    pyx_funcs: list[str] = []
    py_exports: list[str] = []
    pyi_lines: list[str] = [CYDIST_MODULE_DOC, "", "import numpy as np", ""]

    prev_category: str | None = None
    for spec in specs:
        if spec.category_path != prev_category:
            if prev_category is not None:
                pyx_funcs.append("")
            pyx_funcs.append(_category_banner(spec.category_path))
            pyx_funcs.append("")
            prev_category = spec.category_path

        out_c = "int" if spec.discrete else "double"
        fused_continuous = spec.vault_id in FUSED_CONTINUOUS_VAULT_IDS and not spec.discrete
        out_pyx = "cnp.int32_t" if spec.discrete else ("ContinuousOut" if fused_continuous else "cnp.float64_t")

        c_params: list[str] = []
        pyx_sig_params: list[str] = [f"{out_pyx}[:] out"]
        py_sig_params: list[str] = ["out: np.ndarray"]
        ctor_args: list[str] = []
        shim_body_prefix: list[str] = []

        for name, c_type, kind in spec.params:
            safe = safe_param_name(name)
            if kind == "probs":
                c_params.append(f"const double* {safe}")
                c_params.append("int k")
                pyx_sig_params.append("cnp.float64_t[:] probs")
                py_sig_params.append("probs: np.ndarray")
                shim_body_prefix = [
                    f"    std::vector<double> pv({safe}, {safe} + k);",
                    f"    distributions::{spec.cpp_class} dist(std::move(pv));",
                ]
            elif kind == "scalar_int":
                c_params.append(f"int {safe}")
                pyx_sig_params.append(f"int {safe}")
                py_sig_params.append(f"{safe}: int")
                ctor_args.append(safe)
            else:
                c_params.append(f"double {safe}")
                pyx_sig_params.append(f"double {safe}")
                py_sig_params.append(f"{safe}: float")
                ctor_args.append(safe)

        c_params += ["uint64_t seed", f"{out_c}* out", "size_t n_samples"]
        pyx_sig_params.append("uint64_t seed=42")
        py_sig_params.append("seed: int = 42")

        body = _cydist_ctor_body(spec, ctor_args, shim_body_prefix, spec.cpp_class)
        _append_cydist_shim(
            c_func=spec.c_func,
            c_params=c_params,
            body=body,
            shim_decls=shim_decls,
            shim_impl=shim_impl,
        )

        if fused_continuous:
            c_func_f32 = f"{spec.c_func}_f32"
            float_cpp = FUSED_FLOAT_CPP_CLASS[spec.vault_id]
            c_params_f32 = c_params[:-3] + ["uint64_t seed", "float* out", "size_t n_samples"]
            body_f32 = _cydist_ctor_body(spec, ctor_args, shim_body_prefix, float_cpp)
            _append_cydist_shim(
                c_func=c_func_f32,
                c_params=c_params_f32,
                body=body_f32,
                shim_decls=shim_decls,
                shim_impl=shim_impl,
            )

        pyx_c_params: list[str] = []
        for name, c_type, kind in spec.params:
            safe = safe_param_name(name)
            if kind == "probs":
                pyx_c_params.append(f"const double* {safe}")
                pyx_c_params.append("int k")
            else:
                pyx_c_params.append(f"{c_type} {safe}")
        pyx_c_params += ["uint64_t seed", f"{out_c}* out", "size_t n_samples"]
        pyx_extern.append(
            f"    void {spec.c_func}({', '.join(pyx_c_params)}) nogil"
        )
        if fused_continuous:
            pyx_c_params_f32 = pyx_c_params[:-3] + ["uint64_t seed", "float* out", "size_t n_samples"]
            pyx_extern.append(
                f"    void {c_func_f32}({', '.join(pyx_c_params_f32)}) nogil"
            )

        call_args: list[str] = []
        setup: list[str] = ["    cdef size_t n_samples = <size_t>out.shape[0]"]
        for name, _c_type, kind in spec.params:
            safe = safe_param_name(name)
            if kind == "probs":
                setup.append("    cdef const double* probs_ptr = <const double*>&probs[0]")
                setup.append("    cdef int k = <int>probs.shape[0]")
                call_args.extend(["probs_ptr", "k"])
            else:
                call_args.append(safe)

        pyx_funcs.append(f"def {spec.py_func}({', '.join(pyx_sig_params)}):")
        if fused_continuous:
            setup.extend(
                [
                    "    cdef float* ptr_f32",
                    "    cdef double* ptr_f64",
                ]
            )
            pyx_funcs.extend(setup)
            call_f64 = call_args + ["seed", "ptr_f64", "n_samples"]
            call_f32 = call_args + ["seed", "ptr_f32", "n_samples"]
            pyx_funcs.append("    if ContinuousOut is cnp.float32_t:")
            pyx_funcs.append("        ptr_f32 = &out[0]")
            pyx_funcs.append("        with nogil:")
            pyx_funcs.append(f"            {c_func_f32}({', '.join(call_f32)})")
            pyx_funcs.append("    else:")
            pyx_funcs.append("        ptr_f64 = &out[0]")
            pyx_funcs.append("        with nogil:")
            pyx_funcs.append(f"            {spec.c_func}({', '.join(call_f64)})")
        else:
            setup.append(f"    cdef {out_c}* ptr = <{out_c}*>&out[0]")
            pyx_funcs.extend(setup)
            call_args.extend(["seed", "ptr", "n_samples"])
            pyx_funcs.append("    with nogil:")
            pyx_funcs.append(f"        {spec.c_func}({', '.join(call_args)})")
        pyx_funcs.append("")

        py_exports.append(spec.py_func)
        doc = _func_docstring(spec)
        if fused_continuous:
            doc += " Accepts float32 or float64 ``out`` (FusedType dispatch)."
        pyi_lines.append(f"def {spec.py_func}({', '.join(py_sig_params)}) -> None:")
        pyi_lines.append(f'    """{doc}"""')
        pyi_lines.append("")

    (CYDIST / "cydist_shim.h").write_text(
        "#pragma once\n// Generated by tools/codegen_distributions.py\n\n"
        "#include <stddef.h>\n#include <stdint.h>\n\n"
        "#ifdef __cplusplus\nextern \"C\" {\n#endif\n\n"
        + "\n".join(shim_decls)
        + "\n\n#ifdef __cplusplus\n}\n#endif\n"
    )

    (CYDIST / "cydist_shim.cpp").write_text(
        "// Generated by tools/codegen_distributions.py\n"
        '#include "distributions/all.hpp"\n\n'
        "#include <cstddef>\n#include <cstdint>\n#include <vector>\n\n"
        "extern \"C\" {\n\n"
        + "\n".join(shim_impl)
        + "}  // extern \"C\"\n"
    )

    (CYDIST / "cydist.pyx").write_text(
        "# cython: language_level=3\n"
        + CYDIST_PYX_MODULE_DOC
        + "\n"
        "cimport numpy as cnp\n"
        "from libc.stddef cimport size_t\n"
        "from libc.stdint cimport uint64_t\n\n"
        "cnp.import_array()\n\n"
        + (
            "ctypedef fused ContinuousOut:\n"
            "    cnp.float32_t\n"
            "    cnp.float64_t\n\n"
            if any(s.vault_id in FUSED_CONTINUOUS_VAULT_IDS for s in specs)
            else ""
        )
        + 'cdef extern from "cydist_shim.h":\n'
        + "\n".join(pyx_extern)
        + "\n\n"
        + "\n".join(pyx_funcs)
    )

    init_exports = ",\n    ".join(py_exports)
    (CYDIST / "__init__.py").write_text(
        '"""Python bindings for distribution samplers."""\n\n'
        "from cydist._native import (\n    "
        + ",\n    ".join(py_exports)
        + ",\n)\n\n"
        f"__all__: list[str] = [\n    {init_exports},\n]\n"
    )

    pyi_lines.append("__all__: list[str] = [")
    pyi_lines.extend(f'    "{name}",' for name in py_exports)
    pyi_lines.append("]")
    (CYDIST / "__init__.pyi").write_text("\n".join(pyi_lines) + "\n")

    pxd_lines = [
        CYDIST_PXD_HEADER.format(n=len(specs)).rstrip(),
        "from libc.stdint cimport uint64_t",
        "from libc.stddef cimport size_t",
        "",
        'cdef extern from "cydist_shim.h":',
        *pyx_extern,
    ]
    (CYDIST / "cydist.pxd").write_text("\n".join(pxd_lines) + "\n")


def emit_registry_tiers(registry: list[dict], recipes: dict[str, Recipe]) -> None:
    """Write sampler_tier into registry.yaml for each distribution."""
    path = ROOT / ".vault" / "_meta" / "registry.yaml"
    data = yaml.safe_load(path.read_text())
    for entry in data["distributions"]:
        vid = entry["id"]
        if vid in MANUAL:
            entry["sampler_tier"] = "hand-written"
        elif vid in recipes:
            entry["sampler_tier"] = recipes[vid].sampler_tier
        else:
            entry["sampler_tier"] = "family"
    path.write_text(yaml.dump(data, sort_keys=False, allow_unicode=True, default_flow_style=False))
    tiers = {e["sampler_tier"] for e in data["distributions"]}
    print(f"  registry: sampler_tier updated ({', '.join(sorted(tiers))})")


