def py_kwargs_literal(spec: CydistSpec, recipe: Recipe | None) -> str:
    if spec.vault_id in MANUAL_PY_KWARGS:
        return MANUAL_PY_KWARGS[spec.vault_id]
    if recipe is None or not recipe.bench_ctor_args:
        return "{}"
    vals = [v.strip() for v in recipe.bench_ctor_args.split(",")]
    parts: list[str] = []
    for (pname, _, kind), val in zip(spec.params, vals, strict=False):
        pyname = safe_param_name(pname)
        if kind == "scalar_int":
            parts.append(f'"{pyname}": {int(float(val))}')
        else:
            parts.append(f'"{pyname}": {float(val)}')
    return "{" + ", ".join(parts) + "}"


def emit_tests(registry: list[dict], recipes: dict[str, Recipe], specs: list[CydistSpec]) -> None:
    alias = {"normal-gaussian": "normal", "students-t": "student-t"}
    bench_ids: list[str] = []
    seen: set[str] = set()
    for entry in registry:
        bench_id = alias.get(entry["id"], entry["id"])
        if bench_id in seen:
            continue
        seen.add(bench_id)
        bench_ids.append(bench_id)

    cydist_cases: list[str] = []
    for spec in specs:
        recipe = recipes.get(spec.vault_id)
        kwargs = py_kwargs_literal(spec, recipe)
        cydist_cases.append(
            f'    ("{spec.py_func}", {kwargs}, {spec.discrete!r}),'
        )

    tests_dir = ROOT / "tests"
    tests_dir.mkdir(exist_ok=True)
    (tests_dir / "test_generated.py").write_text(
        '"""Generated smoke tests for all vault distributions — do not edit by hand."""\n\n'
        "from __future__ import annotations\n\n"
        "import subprocess\n"
        "from pathlib import Path\n\n"
        "import numpy as np\n"
        "import pytest\n\n"
        "import cydist\n\n"
        f"ROOT = Path(__file__).resolve().parents[1]\n"
        f"RUN_BENCH = ROOT / \"build\" / \"run_bench\"\n\n"
        f"BENCH_IDS: list[str] = [\n"
        + "\n".join(f'    "{bid}",' for bid in bench_ids)
        + "\n]\n\n"
        f"CYDIST_CASES: list[tuple[str, dict, bool]] = [\n"
        + "\n".join(cydist_cases)
        + "\n]\n\n"
        "def test_cydist_export_count() -> None:\n"
        f"    assert len(cydist.__all__) == {len(specs)}\n\n\n"
        "@pytest.mark.parametrize(\"dist_id\", BENCH_IDS)\n"
        "def test_cpp_run_bench(dist_id: str) -> None:\n"
        "    assert RUN_BENCH.exists(), \"run make build first\"\n"
        "    subprocess.run(\n"
        "        [str(RUN_BENCH), dist_id, \"cpp\", \"1000\", \"42\", \"--csv\"],\n"
        "        check=True,\n"
        "        cwd=ROOT,\n"
        "    )\n\n\n"
        "@pytest.mark.parametrize(\"func_name,kwargs,discrete\", CYDIST_CASES)\n"
        "def test_cydist_sample_batch(func_name: str, kwargs: dict, discrete: bool) -> None:\n"
        "    fn = getattr(cydist, func_name)\n"
        "    dtype = np.int32 if discrete else np.float64\n"
        "    out = np.zeros(128, dtype=dtype)\n"
        "    fn(out, seed=42, **kwargs)\n"
        "    if discrete:\n"
        "        assert out.size == 128\n"
        "    else:\n"
        "        assert np.all(np.isfinite(out))\n"
    )
    print(f"  tests: {len(bench_ids)} C++ + {len(specs)} Python smoke tests")


